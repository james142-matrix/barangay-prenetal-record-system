    private void addDateInputRestriction(javax.swing.JTextField textField) {
    textField.addKeyListener(new java.awt.event.KeyAdapter() {
        @Override
        public void keyTyped(java.awt.event.KeyEvent evt) {
            char c = evt.getKeyChar();
            String currentText = textField.getText();

            if (!Character.isDigit(c) && c != '/' && c != '\b') {
                evt.consume();
                return;
            }

            if (Character.isDigit(c)) {
                if (currentText.length() == 2 || currentText.length() == 5) {
                    textField.setText(currentText + "/");
                }
            }

            if (currentText.length() >= 10 && c != '\b') {
                evt.consume();
            }
        }
    });
}
// ✅ Allow only numbers (no letters, symbols)
private void addNumericRestriction(javax.swing.JTextField textField) {
    textField.addKeyListener(new java.awt.event.KeyAdapter() {
        @Override
        public void keyTyped(java.awt.event.KeyEvent evt) {
            char c = evt.getKeyChar();
            if (!Character.isDigit(c) && c != '\b') {
                evt.consume(); // block non-digit characters
            }
        }
    });
}

private java.sql.Date parseDate(String dateText) {
    if (dateText == null || dateText.trim().isEmpty()) {
        return null;
    }

    try {
        // Try MM/dd/yyyy first
        java.time.LocalDate localDate = java.time.LocalDate.parse(dateText, java.time.format.DateTimeFormatter.ofPattern("MM/dd/yyyy"));
        return java.sql.Date.valueOf(localDate);
    } catch (Exception e) {
        try {
            // Try yyyy-MM-dd (database format)
            java.time.LocalDate localDate = java.time.LocalDate.parse(dateText, java.time.format.DateTimeFormatter.ofPattern("yyyy-MM-dd"));
            return java.sql.Date.valueOf(localDate);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "⚠ Invalid date format: " + dateText);
            return null;
        }
    }
}
    
