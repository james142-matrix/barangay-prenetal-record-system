    private void loadPatientData() {
        try (Connection conn = DBConnection.getConnection()) {
            String sql = "SELECT * FROM patient_information WHERE patient_id = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, patientId);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                txtFirstName.setText(rs.getString("first_name"));
                txtLastName.setText(rs.getString("last_name"));
                txtAge.setText(String.valueOf(rs.getInt("age")));
                txtBirthDate.setText(rs.getString("birth_date"));
                txtAddress.setText(rs.getString("address"));
                txtContactNo.setText(rs.getString("contact_no"));
                cbBloodType.setSelectedItem(rs.getString("blood_type"));
            }

            String sql2 = "SELECT * FROM pregnancy_information WHERE patient_id = ?";
            PreparedStatement pst2 = conn.prepareStatement(sql2);
            pst2.setInt(1, patientId);
            ResultSet rs2 = pst2.executeQuery();

            if (rs2.next()) {
                txtGravida.setText(String.valueOf(rs2.getInt("gravida")));
                txtPara.setText(String.valueOf(rs2.getInt("para")));
                txtLMP.setText(rs2.getString("lmp"));
                txtEED.setText(rs2.getString("eed"));;;;
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading patient data: " + e.getMessage());
        }
    }
    
    // 🔹 Load all midwives into the combo box
private void loadMidwives() {
    try (Connection conn = DBConnection.getConnection()) {
        String sql = "SELECT midwife_id, full_name FROM midwife_information";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        cbMidwives.removeAllItems(); // clear combo box

        while (rs.next()) {
            // Example display: "1 - Maria Santos"
            cbMidwives.addItem(rs.getInt("midwife_id") + " - " + rs.getString("full_name"));
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading midwives: " + e.getMessage());
    }
}


