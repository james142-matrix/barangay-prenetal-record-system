    private void loadPatients() {
    try (Connection conn = DBConnection.getConnection()) {
        String sql = "SELECT patient_id, CONCAT(first_name, ' ', last_name) AS fullname FROM patient_information";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();
        cmbPatient.removeAllItems();
        while (rs.next()) {
            cmbPatient.addItem(rs.getInt("patient_id") + " - " + rs.getString("fullname"));
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading patients: " + e.getMessage());
    }
}
    
    private void addNumberOnly(javax.swing.JTextField textField) {
    textField.addKeyListener(new java.awt.event.KeyAdapter() {
        @Override
        public void keyTyped(java.awt.event.KeyEvent evt) {
            char c = evt.getKeyChar();
            if (!Character.isDigit(c) && c != '.' && c != '\b') {
                evt.consume(); // ignore invalid input
            }
        }
    });
}
    private void restrictDateInput(javax.swing.JTextField textField) {
    textField.addKeyListener(new java.awt.event.KeyAdapter() {
        public void keyTyped(java.awt.event.KeyEvent evt) {
            char c = evt.getKeyChar();
            if (!Character.isDigit(c) && c != '/' && c != java.awt.event.KeyEvent.VK_BACK_SPACE) {
                evt.consume(); // Block invalid character
            }
        }
    });
}
    
