    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
    try (Connection conn = DBConnection.getConnection()) {
        String selectedPatient = (String) cmbPatient.getSelectedItem();

        if (selectedPatient == null || selectedPatient.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a patient.");
            return;
        }

        // ✅ Get patient ID
        int patientId = Integer.parseInt(selectedPatient.split(" - ")[0]);

        // ✅ Validate required fields
        if (txtDate.getText().trim().isEmpty() ||
            txtBP.getText().trim().isEmpty() ||
            txtWeight.getText().trim().isEmpty() ||
            txtFHR.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all required fields before saving.");
            return;
        }

        // ✅ Convert MM/dd/yyyy → yyyy-MM-dd
        String inputDate = txtDate.getText().trim();
        java.text.SimpleDateFormat inputFormat = new java.text.SimpleDateFormat("MM/dd/yyyy");
        java.text.SimpleDateFormat outputFormat = new java.text.SimpleDateFormat("yyyy-MM-dd");
        String formattedDate = outputFormat.format(inputFormat.parse(inputDate));

        // ✅ SQL Insert
        String sql = "INSERT INTO checkup_details (patient_id, date_of_visit, blood_pressure, weight, fetal_hr, notes) VALUES (?, ?, ?, ?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setInt(1, patientId);
        pst.setString(2, formattedDate);
        pst.setString(3, txtBP.getText());
        pst.setDouble(4, Double.parseDouble(txtWeight.getText()));
        pst.setInt(5, Integer.parseInt(txtFHR.getText()));
        pst.setString(6, txtNotes.getText());

        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Checkup saved successfully!");

        // ✅ Clear fields after saving
        txtDate.setText("");
        txtBP.setText("");
        txtWeight.setText("");
        txtFHR.setText("");
        txtNotes.setText("");
        cmbPatient.setSelectedIndex(-1);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error saving checkup: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSaveActionPerformed

